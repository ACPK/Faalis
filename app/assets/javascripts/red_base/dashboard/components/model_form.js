Dashboard.Field = Ember.Object.extend({
});


Dashboard.ModelFormComponent = Ember.Component.extend({

    get_fields: function(Model){
        var form   = {},
            fields = Ember.get(Model, "fields") || [];

        if ("form" in Model) {
            Ember.Logger.log("Model object has 'form' inside: ", Model);
            form = Model.form;
        }

        fields.forEach(function(field, type){
            var f = Dashboard.Field.create({
                name: field,
                type: type
            });

            if (field in form) {
                f.set("");
            }
        });
    },
    fields: function(){
        var Model = new this.model;
        var fields = this.get_fields(Model);
        return [1,3];

    }.property(),

});
